package Model;

public class AutomobileModel {
    private int id;
    private int year;
    private String name;

    public AutomobileModel(int id,String name) {
        this.id = id;
        this.name = name;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

